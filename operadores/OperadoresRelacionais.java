package operadores;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int numero1 = 1;
        int numero2 = 2;

        System.out.println("numero1 == numero2: " + (numero1 == numero2));
        System.out.println("numero1 != numero2: " + (numero1 != numero2));
        System.out.println("numero1 > numero2: " + (numero1 > numero2));
        System.out.println("numero1 < numero2: " + (numero1 < numero2));
        System.out.println("numero1 >= numero2: " + (numero1 >= numero2));
        System.out.println("numero1 <= numero2: " + (numero1 <= numero2));
    }
}
