package treinando_classes.classe_empresa;

public class ClasseDepartamento {
    private String nomeDepartamento;
    private String FuncionariosDepartamento;

    public String getNomeDepartamento() {
        return nomeDepartamento;
    }

    public void setNomeDepartamento(String nomeDepartamento) {
        this.nomeDepartamento = nomeDepartamento;
    }

    public String getFuncionariosDepartamento() {
        return FuncionariosDepartamento;
    }

    public void setFuncionariosDepartamento(String funcionariosDepartamento) {
        FuncionariosDepartamento = funcionariosDepartamento;
    } 
}
